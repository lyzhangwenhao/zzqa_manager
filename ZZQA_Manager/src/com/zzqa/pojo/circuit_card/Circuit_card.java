package com.zzqa.pojo.circuit_card;

import java.io.Serializable;

public class Circuit_card implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6166969450053178505L;
	private int id;
	private int device_id;
	private String name;
	private String sn;
	private int type;//0：设备板子；1：模板的板子
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getDevice_id() {
		return device_id;
	}
	public void setDevice_id(int device_id) {
		this.device_id = device_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
}
