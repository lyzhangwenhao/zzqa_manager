package com.zzqa.pojo.equipment_template;

import java.io.Serializable;
import java.util.List;

import com.zzqa.pojo.circuit_card.Circuit_card;

public class Equipment_template implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 333370155117944859L;
	private int id;
	private String alias;
	private List<Circuit_card> circuit_cards;
	public List<Circuit_card> getCircuit_cards() {
		return circuit_cards;
	}
	public void setCircuit_cards(List<Circuit_card> circuit_cards) {
		this.circuit_cards = circuit_cards;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
}
