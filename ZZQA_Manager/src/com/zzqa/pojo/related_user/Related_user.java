package com.zzqa.pojo.related_user;

import java.io.Serializable;
/*****
 * 用户关联表
 * @author louph
 *
 */
public class Related_user implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -155285232968487690L;
	private int id;
	private int type;//1:建议（被私信的对象）：2：评论点赞：3：回复点赞
	private int foreign_id;
	private int uid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getForeign_id() {
		return foreign_id;
	}
	public void setForeign_id(int foreign_id) {
		this.foreign_id = foreign_id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
}
